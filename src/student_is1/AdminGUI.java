    public void refreshStudentsTab(){
        
        DefaultTableModel model = (DefaultTableModel) studentsTBL.getModel();
        model.setRowCount(0);
        
        for(Student student : impl.read_all()){
            model.addRow(new Object[]{
                student.getStudent_id(),
                student.getLastname() + ", " + student.getFirstname(),
                student.getGender(),
                student.getGrade_level() + " - " + student.getSection(),
                student.getBirthdate(),
                student.getDatetime()
            });
        }
    }
    
    public void initialData(){
        
        DefaultTableModel model = (DefaultTableModel) studentsTBL.getModel();
        
        for(int i = 0; i<=10; i++){
            int id = i;
            String name = "Nelson_" + i;
            String gender = "Male_" + i;
            String grade = i + " - ICT";
            String birthday = "birthday";
            String datetime = "Today";
            
            model.addRow(new Object[]{
                id,
                name,
                gender,
                grade,
                birthday,
                datetime
            });
        }
    }
    
    public void refillForm(){
        fnTF.setText(this.student.getFirstname());
        lnTF.setText(student.getLastname());
        gradeTF.setText(student.getGrade_level() + "");
        sectionTF.setText(student.getSection());
        bdTF.setText(student.getBirthdate());
        
        if(student.getGender() == "Male"){
            male.setSelected(true);
        }else{
            female.setSelected(true);
        }
    }
