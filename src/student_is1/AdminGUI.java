    private void deleteMIActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteMIActionPerformed
        // TODO add your handling code here:
        JOptionPane.showMessageDialog(null, "Are you sure to delete this student?", 
                "System Confirmation", JOptionPane.OK_CANCEL_OPTION);
        
        DefaultTableModel model = (DefaultTableModel) studentsTBL.getModel();
        int id = Integer.parseInt(model.getValueAt(studentsTBL.getSelectedRow(), 0).toString());
        
        if(JOptionPane.OK_OPTION == 0){
            if(impl.delete(id)){
                JOptionPane.showMessageDialog(null, "Student Deleted Successfully!", 
                "System Message", JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(null, "Student Deletion Failed!", 
                "System Message", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_deleteMIActionPerformed

