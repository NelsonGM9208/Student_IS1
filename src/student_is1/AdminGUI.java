    private void saveBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBTNActionPerformed
        // TODO add your handling code here:
        String firstname = fnTF.getText();
        String lastname = lnTF.getText();
        String gender;
        int grade_level = Integer.parseInt(gradeTF.getText());
        String section = sectionTF.getText();
        String birthdate = bdTF.getText();

        if(male.isSelected()){
            gender = "Male";
        }else{
            gender = "Female";
        }
        
        if(saveBTN.getText() == "Save"){
            this.student = new Student(0, firstname, lastname, gender, grade_level, section, birthdate, "");

            if(impl.create(this.student) == true){
                JOptionPane.showMessageDialog(null, "Student Added!", "Notification", JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(null, "Student Added!", "Notification", JOptionPane.INFORMATION_MESSAGE);
            }
        }else{
            this.student = new Student(0, firstname, lastname, gender, grade_level, section, birthdate, "");
            
            if(impl.update(this.student) == true){
                JOptionPane.showMessageDialog(null, "Student Updated!", "Notification", JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(null, "Student Updated!", "Notification", JOptionPane.INFORMATION_MESSAGE);
            }
            
            saveBTN.setText("Save");
            resetBTN.setText("Reset");
        }
    }//GEN-LAST:event_saveBTNActionPerformed

